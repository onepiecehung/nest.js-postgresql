/**
 * Record mapping country codes to country names for O(1) lookup.
 * Useful when you need to quickly get a country name by its code.
 * Example: COUNTRY_CODE_MAP['US'] returns 'United States of America'
 */
export const COUNTRY_CODE_MAP: Readonly<Record<string, string>> = Object.freeze(
  COUNTRY_CODES.reduce(
    (acc, country) => {
      acc[country.code] = country.name;
      return acc;
    },
    {} as Record<string, string>,
  ),
);

/**
 * Type for valid ISO 3166-1 alpha-2 country codes.
 * Extracted from the COUNTRY_CODES array for type safety.
 */
export type CountryCodeType = (typeof COUNTRY_CODES)[number]['code'];

/**
 * Helper function to get country name by code.
 * Returns undefined if the code is not found.
 *
 * @param code - ISO 3166-1 alpha-2 country code (e.g., 'US', 'JP', 'VN')
 * @returns Country name or undefined if not found
 */
export function getCountryName(code: string): string | undefined {
  return COUNTRY_CODE_MAP[code.toUpperCase()];
}

/**
 * Helper function to check if a country code is valid.
 *
 * @param code - ISO 3166-1 alpha-2 country code to validate
 * @returns True if the code exists in the country list
 */
export function isValidCountryCode(code: string): boolean {
  return code.toUpperCase() in COUNTRY_CODE_MAP;
}
